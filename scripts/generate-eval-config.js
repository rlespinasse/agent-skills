#!/usr/bin/env node
"use strict";

// Generates promptfoo configuration from skill evals.json files.
//
// Outputs:
//   promptfooconfig.yaml  — main config (provider, grader, comments)
//   promptfoo-tests.json  — test cases (generated from evals.json)
//
// Usage:
//   node scripts/generate-eval-config.js              # all skills
//   node scripts/generate-eval-config.js skill-name   # single skill

const fs = require("fs");
const path = require("path");

const skillsDir = path.resolve(__dirname, "..", "skills");
const configFile = path.resolve(__dirname, "..", "promptfooconfig.yaml");
const testsFile = path.resolve(__dirname, "..", "promptfoo-tests.json");

const targetSkill = process.argv[2] || "";

// Collect skills with evals
const skillDirs = fs.readdirSync(skillsDir).filter((d) => {
  const fullPath = path.join(skillsDir, d);
  if (!fs.statSync(fullPath).isDirectory()) return false;
  if (targetSkill && d !== targetSkill) return false;
  return fs.existsSync(path.join(fullPath, "evals", "evals.json"));
});

if (skillDirs.length === 0) {
  console.error(targetSkill ? `No evals found for skill: ${targetSkill}` : "No evals found in any skill");
  process.exit(1);
}

// Generate test cases from evals
const tests = [];

for (const skillName of skillDirs) {
  const evalsPath = path.join(skillsDir, skillName, "evals", "evals.json");
  const evals = JSON.parse(fs.readFileSync(evalsPath, "utf-8"));

  for (const e of evals) {
    const rubricLines = e.assertions.map((a, i) => `${i + 1}. ${a}`).join("\n");

    tests.push({
      description: `[${skillName}] ${e.id}`,
      vars: {
        prompt: e.prompt,
        context_files: e.files.join(","),
      },
      assert: [
        {
          type: "llm-rubric",
          value: `The response must satisfy ALL of the following criteria:\n${rubricLines}`,
        },
      ],
    });
  }
}

// Write test cases as JSON (reliable, no YAML escaping issues)
fs.writeFileSync(testsFile, JSON.stringify(tests, null, 2) + "\n", "utf-8");

// Write YAML config (static structure with comments)
const description = targetSkill ? `Agent Skills Evals (${targetSkill})` : "Agent Skills Evals";

const yaml = `# Auto-generated by scripts/generate-eval-config.js — do not edit manually.
#
# This configuration uses Claude Code CLI as both the response provider and
# the LLM judge (via llm-rubric). No API key is needed — it runs entirely
# through a Claude Code subscription (Max 5x or higher recommended).
#
# NOTE: Each eval requires ~2 Claude Code calls (generate + judge).
# Running the full suite (~${tests.length} evals) takes significant rate limit budget.
#
# Want to help run evals faster via the Anthropic API?
# Consider sponsoring this project: https://github.com/sponsors/rlespinasse
#
# Usage:
#   npx -y promptfoo eval        # run evals
#   npx -y promptfoo view        # view results in browser

description: "${description}"

prompts:
  - "{{prompt}}"

providers:
  - id: "file://scripts/eval-provider.js"
    label: "Claude Code"

defaultTest:
  options:
    provider:
      id: "file://scripts/eval-provider.js"

tests: file://promptfoo-tests.json
`;

fs.writeFileSync(configFile, yaml, "utf-8");

console.log(`Generated ${tests.length} tests from ${skillDirs.length} skill(s)`);
console.log(`  ${configFile}`);
console.log(`  ${testsFile}`);
